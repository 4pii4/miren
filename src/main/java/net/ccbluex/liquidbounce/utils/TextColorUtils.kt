package net.ccbluex.liquidbounce.utils

object TextColorUtils {
    fun darkRed(s: String) = "§4$s§r"
    fun red(s: String) = "§c$s§r"
    fun gold(s: String) = "§6$s§r"
    fun yellow(s: String) = "§e$s§r"
    fun darkGreen(s: String) = "§2$s§r"
    fun green(s: String) = "§a$s§r"
    fun aqua(s: String) = "§b$s§r"
    fun darkAqua(s: String) = "§3$s§r"
    fun darkBlue(s: String) = "§1$s§r"
    fun blue(s: String) = "§9$s§r"
    fun lightPurple(s: String) = "§d$s§r"
    fun darkPurple(s: String) = "§5$s§r"
    fun white(s: String) = "§f$s§r"
    fun gray(s: String) = "§7$s§r"
    fun darkGray(s: String) = "§8$s§r"
    fun black(s: String) = "§0$s§r"

}